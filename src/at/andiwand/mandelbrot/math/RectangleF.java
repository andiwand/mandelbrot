package at.andiwand.mandelbrot.math;

public class RectangleF {

    public float x;
    public float y;
    public float width;
    public float height;

    public RectangleF(float x, float y, float width, float height) {
	this.x = x;
	this.y = y;
	this.width = width;
	this.height = height;
    }

}