package at.andiwand.mandelbrot.math;

public class RectangleD {

    public double x;
    public double y;
    public double width;
    public double height;

    public RectangleD(double x, double y, double width, double height) {
	this.x = x;
	this.y = y;
	this.width = width;
	this.height = height;
    }

}